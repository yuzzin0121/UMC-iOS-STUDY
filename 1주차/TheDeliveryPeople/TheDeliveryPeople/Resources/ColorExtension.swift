import UIKit

extension UIColor { // 배달의 민족 색상 정보
    @nonobjc class var mint: UIColor {
        return UIColor(red: 94.0 / 255.0, green: 190.0 / 255.0, blue: 187.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var backgroundGray: UIColor {
        return UIColor(red: 242.0 / 255.0, green: 244.0 / 255.0, blue: 246.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var gray: UIColor {
        return UIColor(red: 217.0 / 255.0, green: 217.0 / 255.0, blue: 217.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var borderGray: UIColor {
        return UIColor(red: 230.0 / 255.0, green: 230.0 / 255.0, blue: 230.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var buttonGray: UIColor {
        return UIColor(red: 243.0 / 255.0, green: 245.0 / 255.0, blue: 247.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var buttonBorderGray: UIColor {
        return UIColor(red: 231.0 / 255.0, green: 233.0 / 255.0, blue: 234.0 / 255.0, alpha: 1.0)
    }
}
